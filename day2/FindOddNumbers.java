public class FindOddNumbers {

	public static void main(String[] args) {
		for (int i = 0; i < 11; i++) {
			if (i%2==0) {
				System.out.println(i+" is an even number");
				
			} else {
				System.out.println(i+" is an odd number");

			}
			
		}

	}

}
